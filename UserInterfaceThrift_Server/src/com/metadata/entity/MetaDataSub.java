package com.metadata.entity;

public class MetaDataSub
{
	private String	id;
	private String	subMode;
	private String	defID;
	private String	tokenList;
	private String	createTime;

	/**
	 * @return the id
	 */
	public String getId()
	{
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id)
	{
		this.id = id;
	}

	/**
	 * @return the subMode
	 */
	public String getSubMode()
	{
		return subMode;
	}

	/**
	 * @param subMode the subMode to set
	 */
	public void setSubMode(String subMode)
	{
		this.subMode = subMode;
	}

	/**
	 * @return the defID
	 */
	public String getDefID()
	{
		return defID;
	}

	/**
	 * @param defID the defID to set
	 */
	public void setDefID(String defID)
	{
		this.defID = defID;
	}

	/**
	 * @return the tokenList
	 */
	public String getTokenList()
	{
		return tokenList;
	}

	/**
	 * @param tokenList the tokenList to set
	 */
	public void setTokenList(String tokenList)
	{
		this.tokenList = tokenList;
	}

	/**
	 * @return the createTime
	 */
	public String getCreateTime()
	{
		return createTime;
	}

	/**
	 * @param createTime the createTime to set
	 */
	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}
}
